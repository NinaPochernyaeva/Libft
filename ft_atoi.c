#include "libft.h"

int	ft_atoi(const char *str)
{
	int j;
	int res;

	j = 1;
	res = 0;
	while ((*str > 8 && *str < 13) || *str == '\r' || *str == ' ')
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			j = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		res = res * 10 + *str - 48;
		str++;
	}
	return (j * res);
}
